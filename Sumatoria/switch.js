// Iterador

var nombrePresidente = 'Biden';

switch (nombrePresidente) {
    case 'Putin':
        console.log('Es el presidente de Rusia');
        break;
    case 'Biden':
        console.log('Es el presidente de EEUU');
        break;
    default:
        console.log('No se encontro el presidente');
        break;

}